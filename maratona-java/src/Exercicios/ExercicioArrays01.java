package Exercicios;

public class ExercicioArrays01 {
    static void main() {
        String[] nomes = new String[4];

        nomes [0] = "A";
        nomes [1] = "B";
        nomes [2] = "C";
        nomes [3] = "D";

        for (int i = 0; i < 4; i++) {
            System.out.println(nomes [i]);

        }

    }
}
