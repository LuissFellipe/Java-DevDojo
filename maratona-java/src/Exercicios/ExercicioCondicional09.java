package Exercicios;

public class ExercicioCondicional09 {
    static void main() {
        char sexo = 'M';

        switch (sexo){
            default:
                System.out.println("Invalido");
            case 'M':
                System.out.println("Homem");
                break;
            case 'F':
                System.out.println("Mulher");
                break;
        }





    }
}
